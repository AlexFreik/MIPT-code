'''B. Банковское дело
Ограничение времени	1 секунда
Ограничение памяти	64Mb
Ввод	стандартный ввод или input.txt
Вывод	стандартный вывод или output.txt
Клиент вложил деньги в банк под фиксированный
годовой процент. Необходимо выяснить, сколько денег он получит через несколько лет.

Формат ввода
На вход подаются 3 целых числа, каждое в новой строке:
1. Количество рублей, принесенное в банк 2. Ставка (в процентах) 3. Количество лет

Формат вывода
Выведите число целых рублей, которые получит клиент через запрашиваемое количество лет.

Пример 1
Ввод	Вывод
1000
5
5
1276
Пример 2
Ввод	Вывод
1000
10
10
2593
'''

(amount, rate, years) = (float(input()), float(input()), float(input()))
print(int(amount * ((1 + rate/100) ** years)))

