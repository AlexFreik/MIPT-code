'''D. Панграмма
Ограничение времени	1 секунда
Ограничение памяти	64Mb
Ввод	стандартный ввод или input.txt
Вывод	стандартный вывод или output.txt
На вход подается строка. Нужно проверить, что она является панграммой (https://ru.wikipedia.org/wiki/Панграмма), т.е. содержит все буквы английского языка (не обязательно по одному разу).

Формат ввода
Строка из строчных латиниских букв и пробелов. Гарантируется отсутствие других символов.

Формат вывода
Выведите одно слово - True или False в зависимости от того, является ли строка панграммой.

Пример 1
Ввод	Вывод
hello world
False
Пример 2
Ввод	Вывод
the quick brown fox jumps over the lazy dog
True
'''
lenset = set(input())
alphabet = set('abcdefghijklmnopqrstuvwxyz')
print(lenset.intersection(alphabet) == alphabet)

